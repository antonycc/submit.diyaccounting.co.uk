/**
 * Generate a memorable passphrase of N words joined by hyphens.
 * Uses crypto.randomInt() for secure randomness.
 *
 * @param {number} wordCount - Number of words in the passphrase (default 4)
 * @returns {string} A hyphen-separated passphrase, e.g. "tiger-happy-mountain-silver"
 */
export function generatePassphrase(wordCount = 4) {
  const selected = [];
  for (let i = 0; i < wordCount; i++) {
    selected.push(WORDS[randomInt(WORDS.length)]);
  }
  return selected.join("-");
}

/**
 * Get the size of the wordlist (useful for entropy calculations in tests).
 * @returns {number} The number of words in the wordlist
 */
export function getWordlistSize() {
  return WORDS.length;
}
